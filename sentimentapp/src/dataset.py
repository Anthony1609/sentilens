def load_dataset(csv_path: str = None) -> pd.DataFrame:
    if csv_path and os.path.exists(csv_path):
        df = pd.read_csv(csv_path)
        assert "text" in df.columns and "label" in df.columns
        df = df.dropna(subset=["text","label"])
        df["label"] = df["label"].str.lower().str.strip()
        df = df[df["label"].isin({"positive","negative","neutral"})].reset_index(drop=True)
        print(f"[Dataset] Loaded {len(df)} samples from '{csv_path}'")
    else:
        df = pd.DataFrame(_SEED_DATA, columns=["text","label"])
        print(f"[Dataset] Using built-in seed dataset ({len(df)} samples).")
    return df
